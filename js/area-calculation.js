
// Total Area Of Triangle
function totalAreaOfTriangle(firstValue, secondValue) {
    return 0.5 * firstValue * secondValue;
};


// Total Area Of Rectangle
function totalAreaOfRectangle(firstValue, secondValue) {
    return firstValue * secondValue;
};


